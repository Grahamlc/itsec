package cn.edu.ustc.ttcn.itsec.model;

public class TesterOfSystem implements java.io.Serializable {
	private int autoId;
	
	private Tester tester;
	private Role role;
	private TestSystem testSys;
	public TestSystem getTestSys() {
		return testSys;
	}
	public void setTestSys(TestSystem testSys) {
		this.testSys = testSys;
	}
	public int getAutoId() {
		return autoId;
	}
	public void setAutoId(int autoId) {
		this.autoId = autoId;
	}
	
	public Tester getTester() {
		return tester;
	}
	public void setTester(Tester tester) {
		this.tester = tester;
	}
	public Role getRole() {
		return role;
	}
	public void setRole(Role role) {
		this.role = role;
	}
	
	
}
