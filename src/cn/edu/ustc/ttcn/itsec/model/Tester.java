
package cn.edu.ustc.ttcn.itsec.model;

import java.util.HashSet;
import java.util.Set;

public class Tester implements java.io.Serializable {
	private int userId;

	private String name;
	private String pcont;
	private String mcont;
	private Company company;
	
	private Set<TesterOfSystem> testerToSystem = new HashSet<TesterOfSystem>();

	public Set<TesterOfSystem> getTesterToSystem() {
		return testerToSystem;
	}
	public void setTesterToSystem(Set<TesterOfSystem> testerToSystem) {
		this.testerToSystem = testerToSystem;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPcont() {
		return pcont;
	}
	public void setPcont(String pcont) {
		this.pcont = pcont;
	}
	public String getMcont() {
		return mcont;
	}
	public void setMcont(String mcont) {
		this.mcont = mcont;
	}
	public Company getCompany() {
		return company;
	}
	public void setCompany(Company company) {
		this.company = company;
	}	
	
}

	
	

