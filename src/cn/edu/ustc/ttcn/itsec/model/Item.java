package cn.edu.ustc.ttcn.itsec.model;

public class Item {
	private int itemId;
	private String handleId;
	private String leiBie;
	private int object;
	private String bianHao;
	private String cpZhBiao;
	private String cpxBh;
	private String cpxCon;
	private float weigh;
	private String quAn;
	private String advice;

	public int getItemId() {
		return itemId;
	}

	public void setItemId(int itemId) {
		this.itemId = itemId;
	}

	public String getHandleId() {
		return handleId;
	}

	public void setHandleId(String handleId) {
		this.handleId = handleId;
	}

	public String getLeiBie() {
		return leiBie;
	}

	public void setLeiBie(String leiBie) {
		this.leiBie = leiBie;
	}

	public int getObject() {
		return object;
	}

	public void setObject(int object) {
		this.object = object;
	}

	public String getBianHao() {
		return bianHao;
	}

	public void setBianHao(String bianHao) {
		this.bianHao = bianHao;
	}

	public String getCpZhBiao() {
		return cpZhBiao;
	}

	public void setCpZhBiao(String cpZhBiao) {
		this.cpZhBiao = cpZhBiao;
	}

	public String getCpxBh() {
		return cpxBh;
	}

	public void setCpxBh(String cpxBh) {
		this.cpxBh = cpxBh;
	}

	public String getCpxCon() {
		return cpxCon;
	}

	public void setCpxCon(String cpxCon) {
		this.cpxCon = cpxCon;
	}

	public float getWeigh() {
		return weigh;
	}

	public void setWeigh(float weigh) {
		this.weigh = weigh;
	}

	public String getQuAn() {
		return quAn;
	}

	public void setQuAn(String quAn) {
		this.quAn = quAn;
	}

	public String getAdvice() {
		return advice;
	}

	public void setAdvice(String advice) {
		this.advice = advice;
	}

}
