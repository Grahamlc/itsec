package cn.edu.ustc.ttcn.itsec.model;

import java.util.HashSet;
import java.util.Set;

public class TestObject {
	private int autoId;
	private TestSystem pro;
	private Device device;
	private Objects object;
	private Set<ItemSelRes> testObjectItems = new HashSet<ItemSelRes>();
	private String name;
	public int getAutoId() {
		return autoId;
	}
	public void setAutoId(int autoId) {
		this.autoId = autoId;
	}
	
	public TestSystem getPro() {
		return pro;
	}
	public void setPro(TestSystem pro) {
		this.pro = pro;
	}
	public Device getDevice() {
		return device;
	}
	public void setDevice(Device device) {
		this.device = device;
	}
	
	public Objects getObject() {
		return object;
	}
	public void setObject(Objects object) {
		this.object = object;
	}
	public Set<ItemSelRes> getTestObjectItems() {
		return testObjectItems;
	}
	public void setTestObjectItems(Set<ItemSelRes> testObjectItems) {
		this.testObjectItems = testObjectItems;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	

}
